//初始化PB5和PE5为输出口.并使能这两个口的时钟
//LED IO初始化
void LED_Init(void)
{

	GPIO_InitTypeDef GPIO_InitStructure;

	RCC_AHB1PeriphResetCmd(RCC_AHB1Periph_GPIOB | RCC_AHB1Periph_GPIOE, ENABLE); //使能PB,PE端口时钟

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_5; //LED0-->PB.5 端口配置
	//GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;   //推挽输出
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;	  //输出模式
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;	 //推挽输出
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_DOWN;	 //下拉
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz; //IO口速度为50MHz
	GPIO_Init(GPIOB, &GPIO_InitStructure);			   //根据设定参数初始化GPIOB.5
	GPIO_SetBits(GPIOB, GPIO_Pin_5);				   //PB.5 输出高

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_5; //LED1-->PE.5 端口配置, 推挽输出
	GPIO_Init(GPIOE, &GPIO_InitStructure);	//推挽输出 ，IO口速度为50MHz
	GPIO_SetBits(GPIOE, GPIO_Pin_5);		  //PE.5 输出高

	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOA, ENABLE);	//使能GPIO外设时钟使能
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12 | GPIO_Pin_11; //电机正反输出控制端口
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;			 //输出模式
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;			 //推挽输出
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_DOWN;			 //下拉
	//GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
	GPIO_Init(GPIOA, &GPIO_InitStructure);
}
